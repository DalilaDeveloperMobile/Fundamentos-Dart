main() {
  
  var adicao = (int a, int b) => a + b;
  var subtracao = (int a, int b) => a - b;
  var mutiplicacao = (int a, int b) => a * b;
  var divisao = (int a, int b) => a / b;
  
  print(adicao(4, 19));

  // Resultado
  // 23

  print(subtracao(9, 13));

  // Resultado
  // -4

  print(mutiplicacao(9, 13));

  // Resultado
  // 117

  print(divisao(9, 13));

  // Resultado
  // 0.6923076923076923
}
